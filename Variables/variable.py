# Variable = A container for a value ( string, integer, float, boolean)
#           A variable behaves as if it was the value it contain 


# strings
first_name = "Pradeep"
food = "Pizza"
email = "pradeepkumarbc138@gmail.com"

print(f"Hello, {first_name}")
print(f"I like to eat {food}")
print(f"My email is {email}")

# Integers
age = 21
year = 2006

print(f" I am {age} years old")
print(f" I am born in the year {year}")

# float 
height = 60.8
print(f" My height is { height} feet")

# Boolean
is_student = True
for_sale = True

if for_sale:
    print("That item is for sale")
else:
    print("The item is not for sale")

print(f" Are you a student?: {is_student}")