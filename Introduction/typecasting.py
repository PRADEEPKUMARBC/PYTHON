# TypeCasting = is the process of converting from one data type to another data type
#                str(), int(), float(), bool()

Name = "Pradeepkumar B C" # String
age = 21 # int
height = 5.8 # float
is_student = True # boolean

Name = bool(Name)

print(type(Name))
print(type(age))
print(type(height))
print(type(is_student))